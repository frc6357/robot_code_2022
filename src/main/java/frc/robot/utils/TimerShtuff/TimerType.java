package frc.robot.utils.TimerShtuff;

/**
 * Identifiers for timers used in the transfer subsystem.
 */
public enum TimerType
{
    /**
     * Vertical motor timer identifier.
     */
    VERTICAL,
    /**
     * Eject motor timer identifier.
     */
    EJECT,
    /**
     * Don't do anything identifier
     */
    DEFAULT
}